package baba.names;

/**
 * An enumeration representing different block names for sprites.
 */
public enum BlockName implements SpriteName {

    /**
     * Represents the BABA block.
     */
    BABA,

    /**
     * Represents the FLAG block.
     */
    FLAG,

    /**
     * Represents the FLOWER block.
     */
    FLOWER,

    /**
     * Represents the GRASS block.
     */
    GRASS,

    /**
     * Represents the LAVA block.
     */
    LAVA,

    /**
     * Represents the ROCK block.
     */
    ROCK,

    /**
     * Represents the SKULL block.
     */
    SKULL,

    /**
     * Represents the TILE block.
     */
    TILE,

    /**
     * Represents the WALL block.
     */
    WALL,

    /**
     * Represents the WATER block.
     */
    WATER,

    /**
     * Represents the GLUE block.
     */
    GLUE
}
