package baba.config;

/**
 * Modes enum represents the different modes used during level parsing.
 */
public enum Modes {
  /**
   * Represents the mode for parsing rows.
   */
  ROWS,
  
  /**
   * Represents the mode for parsing columns.
   */
  COLUMNS,
  
  /**
   * Represents the mode for parsing words.
   */
  WORDS,
  
  /**
   * Represents the mode for parsing blocks.
   */
  BLOCKS
}
